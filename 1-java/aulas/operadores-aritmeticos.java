class Main {
    public static void main(String[] args) {
        int n1 = 2;
        int n2 = 3;
        int resultado = 0;

        resultado = n1 + n2; // soma
        System.out.println(resultado);

        resultado = n1 - n2; // subtracao
        System.out.println(resultado);

        resultado = n1 * n2; // multiplicacao
        System.out.println(resultado);

        resultado = n1 / n2; // divisao
        System.out.println(resultado);

        resultado = (int) Math.pow(n1, n2); // potencia
        System.out.println(resultado);

        resultado = n1 % n2; // resto
        System.out.println(resultado);
    }
}