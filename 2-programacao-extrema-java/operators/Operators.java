package operators;

public class Operators {
    public static void main(String[] args) {
        // aritmethics operators
        System.out.println(9+2);
        System.out.println(9-2);
        System.out.println(9*2);
        System.out.println(9/2);
        System.out.println(9.0/2);
        // compound assignment operator
        System.out.println(9%2);
        int value = 9;
        value += 2;
        System.out.println(value); 
        // relational operators
        System.out.println(9 == 2);
        System.out.println(9 != 2);
        System.out.println(9 > 2);
        System.out.println(9 < 2);
    }
}
